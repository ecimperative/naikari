static void spaceport_buyMap( unsigned int wid, char *str )
{
   (void) wid;
   (void) str;
   Outfit *o;
   unsigned int w;

   /* Make sure the map isn't already known, etc. */
   if (land_errDialogue( LOCAL_MAP_NAME, "buyOutfit" ))
      return;

   o = outfit_get( LOCAL_MAP_NAME );
   if (o == NULL) {
      WARN( _("Outfit '%s' does not exist!"), LOCAL_MAP_NAME);
      return;
   }

   player_modCredits( -o->price );
   player_addOutfit(o, 1);

   /* Disable the button. */
   window_disableButtonSoft( land_windows[0], "btnMap" );

   /* Update map quantity in outfitter. */
   w = land_getWid( LAND_WINDOW_OUTFITS );
   if (w > 0)
      outfits_regenList( w, NULL );

   /* Update main tab. */
   land_updateMainTab();
}


/**
 * @brief Saves a snapshot.
 */
static void spaceport_saveSnapshot(unsigned int wid, char *str)
{
   (void) wid;
   (void) str;
   char *annotation;

   /* Do not attempt to save if saving is off. */
   if (player_isFlag(PLAYER_NOSAVE)) {
      dialogue_msgRaw(_("Save Snapshot"),
            _("Cannot save; saving is currently being restricted by a mission"
               " or event."));
      return;
   }

   annotation = dialogue_input(_("Save Snapshot"), 1, 60,
         _("Please enter a name for the snapshot:"));

   if (annotation != NULL)
      save_snapshot(annotation);

   free(annotation);
}


/**
 * @brief Adds the "Buy Local Map" button if needed and updates info.
 */
void land_updateMainTab (void)
{
   char buf[STRMAX], cred[ECON_CRED_STRLEN], tons[STRMAX_SHORT], pop[STRMAX_SHORT];
   Outfit *o;
   long double p = land_planet->population;
   int w, h;
   char scratch[STRMAX_SHORT];
   const char *digits[] = {
      "\xe2\x81\xb0", "\xc2\xb9", "\xc2\xb2", "\xc2\xb3", "\xe2\x81\xb4",
      "\xe2\x81\xb5", "\xe2\x81\xb6", "\xe2\x81\xb7", "\xe2\x81\xb8",
      "\xe2\x81\xb9"};
   int state = 0, COEF = 0, E = 1, EXP = 4;
   size_t l = 0;

   /* Handle the Save Snapshot button. */
   window_enableButton(land_windows[0], "btnSaveSnapshot");
   if (player_isFlag(PLAYER_NOSAVE))
      window_disableButton(land_windows[0], "btnSaveSnapshot");

   if (p < 1.0e3)
      snprintf(pop, sizeof(pop), "%.0Lf", p);
   else {
      snprintf(scratch, sizeof(scratch), "%.1Le", p);
      for (const char *c = scratch; *c; c++) {
         if (state == COEF && *c != 'e')
            l += scnprintf(&pop[l], sizeof(pop)-l, "%c", *c);
         else if (state == COEF ) {
            l += scnprintf(&pop[l], sizeof(pop)-l, "%s", "\xc3\x97" "10");
            state = E;
         }
         else if (state == E && (*c == '+' || *c == '0'))
            state = E;
         else {
            state = EXP;
            l += scnprintf(&pop[l], sizeof(pop)-l, "%s", digits[*c-'0']);
         }
      }
   }

   /* Update credits. */
   tonnes2str(tons, player.p->cargo_free);
   credits2str(cred, player.p->credits, 2);
   snprintf(buf, sizeof(buf),
         _("#nStationed at#0 %s\n"
         "#nSystem:#0 %s\n"
         "#nClass:#0 %s\n"
         "#nFaction:#0 %s\n"
         "#nPopulation:#0 %s\n"
         "\n"
         "#nFree Space:#0 %s\n"
         "#nMoney:#0 %s"),
         _(land_planet->name), _(cur_system->name),
         _(land_planet->class),
         land_planet->faction >= 0 ?
            _(faction_name(land_planet->faction)) : _("None"),
         pop, tons, cred);
   window_dimWidget(land_windows[0], "txtDInfo", &w, &h);
   h = gl_printHeightRaw(&gl_defFont, 200, buf );
   window_resizeWidget(land_windows[0], "txtDInfo", w, h);
   window_modifyText(land_windows[0], "txtDInfo", buf);

   /* Maps are only offered if the planet provides fuel. */
   if (!planet_hasService(land_planet, PLANET_SERVICE_REFUEL))
      return;

   o = outfit_get( LOCAL_MAP_NAME );
   if (o == NULL) {
      WARN( _("Outfit '%s' does not exist!"), LOCAL_MAP_NAME);
      return;
   }

   /* Just enable button if it exists. */
   if (widget_exists( land_windows[0], "btnMap" ))
      window_enableButton( land_windows[0], "btnMap");
   /* Else create it. */
   else {
      /* Refuel button. */
      credits2str( cred, o->price, 0 );
      snprintf( buf, sizeof(buf), _("Buy Local Map (%s)"), cred );
      window_addButtonKey(land_windows[0],
            -20, 20 + 2*(LAND_BUTTON_HEIGHT+20),
            LAND_BUTTON_WIDTH, LAND_BUTTON_HEIGHT, "btnMap",
            buf, spaceport_buyMap, SDLK_b);
   }

   /* Make sure player can click it. */
   if (!outfit_canBuy(LOCAL_MAP_NAME, land_planet))
      window_disableButtonSoft( land_windows[0], "btnMap" );
}


